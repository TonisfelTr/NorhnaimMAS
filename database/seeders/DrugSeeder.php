                // Добавление показаний к применению
                if (!empty($drugData['indications'])) {
                    foreach ($drugData['indications'] as $indicationCode) {
                        $diagnose = Diagnose::where('code', $indicationCode)->first();
                        if ($diagnose) {
                            MedicineIndication::create([
                                'medicine_id' => $drug->id,
                                'diagnose_id' => $diagnose->id,
                            ]);
                        }
                    }
                }

                // Добавление дженериков в таблицу generics
                if (!empty($drugData['generics'])) {
                    foreach ($drugData['generics'] as $generic) {
                        DB::table('generics')->insert([
                            'name'       => $generic,
                            'drug_id'    => $drug->id,
                            'created_at' => Carbon::now(),
                            'updated_at' => Carbon::now(),
                        ]);
                    }
                }

                // Добавление побочных эффектов
                foreach ($drugData['side_effects'] as $sideEffect) {
                    $firstSideEffect = SideEffect::where('code', $sideEffect->value)->first();
                    if ($firstSideEffect) {
                        DB::table('medicine_side_effects')->insert([
                            'drug_id'        => $drug->id,
                            'side_effect_id' => $firstSideEffect->id,
                            'created_at'     => Carbon::now(),
                            'updated_at'     => Carbon::now(),
                        ]);
                    } else {
                        Log::warning("Side effect with code {$sideEffect->value} not found for drug {$drug->name}");
                    }
                }
            }
        } catch (\Exception $e) {
            echo "Ошибка: " . $e->getMessage() . "\n";
        }
    }
}
